    '이태희'=> array('국어'=>100, '영어'=>100, '수학'=>80),
    '박희선'=> array('국어'=>90, '영어'=>100, '수학'=>90),
    '김동건'=> array('국어'=>80, '영어'=>80, '수학'=>100)
