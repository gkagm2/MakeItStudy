echo $arr['이태희']['국어']. ',' .$arr['이태희']['영어']. ',' .$arr['이태희']['수학'];
echo "< br>";
echo $arr['박희선']['국어']. ',' .$arr['박희선']['영어']. ',' .$arr['박희선']['수학'];
echo "< br>";
echo $arr['김동건']['국어']. ',' .$arr['김동건']['영어']. ',' .$arr['김동건']['수학'];

</pre>

